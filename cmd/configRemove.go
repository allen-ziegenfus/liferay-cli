// removeCmd represents the remove command
var removeCmd = &cobra.Command{
	Use:   "remove <key>",
	Short: "Remove a config value",
	Args:  cobra.ExactArgs(1),
	Run: func(cmd *cobra.Command, args []string) {
		Unset(args[0])
	},
}

